#área e perímetro
#inserção dos dados do usuário
alturaR=float(input("Digite a altura da do retangulo em metros: "))
lR=float(input("Digite a largura do retangulo em metro : "))
#calculo de area
areaR=alturaR*lR
perimetroR=(alturaR*2)+(lR*2)
print("area=" +str(areaR)+ " metro\metros")
print("perimetro="+str(perimetroR)+" metro\metros")